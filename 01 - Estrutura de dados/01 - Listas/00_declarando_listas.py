frutas = ["laranja", "maca", "uva"] # Forma mais comum declaração de listas
print(frutas)

frutas = []
print(frutas)

letras = list("python") # Usando o Contructor List, ele vai considerar cada caracter como um elemento
print(letras)

numeros = list(range(10)) # Lista usando a função range
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True] # Lista usando colchetes, aceita strings, booleanos, inteiros, ...
